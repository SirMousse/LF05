# Tuple
# ... Liste die wir nicht verändern können!

rgb_color = (255, 0 , 35)

print(rgb_color[0])     # 255
print(rgb_color[1])     # 0
print(rgb_color[2])     # 35

# Liste = veränderbar -> mutable
# Tuple = unveränderbar -> immutable

# Liste
# ... Python reserviert Speicher dafür, dass Elemente geändert 
#     werden können.

# Tuple
# ... sind kompakter und schneller, weil sie unveränderbar sind.
# verwendet man, wenn man:
# - feste Daten haben möchte/ Daten fest bleiben sollen
# - Daten nicht verändbar sind/ verändert werden dürfen
# - Daten sicher gespeichert werden sollen

# Python erlaubt heterogene Datentypen 
# -> bedeutet, unterschiedliche Datentypen in einer Sammlung