###############################################################################################################################

# Additionszuweisung (Langform x = x + 1 | Kurzform x += 1)
gewicht += 2        # Addier und speicher +=
print(gewicht)

# Zuweisung 
# ... bedeuted ein Wert in einer Variable gespeichert

# Operator 
# ... ist ein Zeichen (+|-|*|/|%|**), das etwas mit Werten macht. Sie führen Aktionen(Berechnungen oder Vergleiche) aus.

# Arithmetischer Operator
# ... Rechenoperator (+|-|*|/|%|**|//)
# Arithmetik bedeutet, rechnen mit Zahlen.

# Zusammengesetzter Operator
# ... mehrere Dinge werden kombiniert. Bsp.: gewicht += 2 (wird gerechnet und gespeichert)

###############################################################################################################################

# Subtraktionszuweisung (Langform x = x - 1 | Kurzform x -= 1)
leckerlis = 20      
leckerlis -= 4      # ziehen ab und speichern -=

print(leckerlis)

##############################################################################################################################

# Multiplikationszuweisung ( Langform x = x * 1 | Kurzform x *= 1)

futter = 5
futter *= 7        # multipliziert und gespeichert *=
print(futter)

##############################################################################################################################

# Divionszuweisung ( Langform x = x / 1 | Kurzform x /= 1)

futter = 36
futter /= 3         # division und speichern /=  
print(futter)