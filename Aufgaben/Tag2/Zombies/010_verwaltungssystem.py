# Aufgabe: Das Zombie-Verwaltungssystem

# In der Zombie-Apokalypse herrscht heute Chaos.
# Schreibt ein Python-Programm, das den Überlebenden hilft.

# 1. Speichert folgende Dinge in Variablen:
# - 6 Zombies
# - 4 Überlebende
# - 3 Schutzbunker

# 2. Berechnet:
# Wie viele Figuren insgesamt unterwegs sind.
# Benutzt den Operator: +

# 3. Verlorene Figuren
# 5 Figuren verschwinden während der Flucht.
# Berechnet:
# Wie viele Figuren noch übrig bleiben.
# Benutzt den Operator: -

# 4. Vorräte berechnen
# Jede Figur bekommt 3 Vorratspakete.
# Berechnet:
# Wie viele Vorratspakete insgesamt benötigt werden.
# Benutzt den Operator: *

# 5. Vorräte gerecht verteilen
# Berechnet:
# Wie viele Vorratspakete jede Figur bekommen würde.
# Benutzt den Operator: /

# 6. Rest berechnen
# Die Vorräte werden in 5er-Kisten verpackt.
# Berechnet:
# Wie viele Vorräte übrig bleiben.
# Benutzt den Operator: %

# 7. Zombie-Mutation
# Berechnet:
# 2 ** 3
# Was kommt dabei heraus?
# Benutzt den Operator: **

# Wichtige Hinweise:
# - Benutzt Variablen
# - Nutzt print() für die Ausgabe
# - Führt euer Programm nach jedem Schritt aus
# - Achtet auf die richtige Schreibweise der Operatoren

