# Aufgabe: Das Tierheim-Verwaltungssystem

# Im Tierheim herrscht heute Chaos.
# Schreibt ein Python-Programm, das dem Tierheim hilft.

# 1. Speichert folgende Tiere in Variablen:
# - 6 Hunde
# - 4 Katzen
# - 3 Kaninchen

# 2. Berechnet:
# Wie viele Tiere insgesamt im Tierheim sind.
# Benutzt den Operator: +

# 3. Vermittelte Tiere
# 5 Tiere werden vermittelt.
# Berechnet:
# Wie viele Tiere noch im Tierheim bleiben.
# Benutzt den Operator: -

# 4. Leckerlis berechnen
# Jedes Tier bekommt 3 Leckerlis.
# Berechnet:
# Wie viele Leckerlis insgesamt benötigt werden.
# Benutzt den Operator: *

# 5. Leckerlis gerecht verteilen
# Berechnet:
# Wie viele Leckerlis jedes Tier bekommt.
# Benutzt den Operator: /

# 6. Rest berechnen
# Die Leckerlis werden in 5er-Tüten verpackt.
# Berechnet:
# Wie viele Leckerlis übrig bleiben.
# Benutzt den Operator: %

# 7. Kaninchen vermehren sich
# Berechnet:
# 2 ** 3
# Was kommt dabei heraus?
# Benutzt den Operator: **

# Wichtige Hinweise:
# - Benutzt Variablen
# - Nutzt print() für die Ausgabe
# - Führt euer Programm nach jedem Schritt aus
# - Achtet auf die richtige Schreibweise der Operatoren

