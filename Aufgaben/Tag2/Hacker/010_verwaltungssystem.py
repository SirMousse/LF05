# Aufgabe: Das Hacker-Verwaltungssystem

# Im sicheren Trainingsnetzwerk herrscht heute Chaos.
# Schreibt ein Python-Programm, das dem Cyber-Team hilft.

# 1. Speichert folgende Dinge in Variablen:
# - 6 Laptops
# - 4 Server
# - 3 Firewalls

# 2. Berechnet:
# Wie viele Geräte insgesamt im Trainingsnetzwerk sind.
# Benutzt den Operator: +

# 3. Gesperrte Geräte
# 5 Geräte werden wegen eines Sicherheitschecks gesperrt.
# Berechnet:
# Wie viele Geräte noch aktiv bleiben.
# Benutzt den Operator: -

# 4. Sicherheitsupdates berechnen
# Jedes Gerät bekommt 3 Sicherheitsupdates.
# Berechnet:
# Wie viele Sicherheitsupdates insgesamt benötigt werden.
# Benutzt den Operator: *

# 5. Sicherheitsupdates gerecht verteilen
# Berechnet:
# Wie viele Sicherheitsupdates jedes Gerät bekommt.
# Benutzt den Operator: /

# 6. Rest berechnen
# Die Sicherheitsupdates werden in 5er-Paketen verteilt.
# Berechnet:
# Wie viele Sicherheitsupdates übrig bleiben.
# Benutzt den Operator: %

# 7. Passwort-Stärke
# Berechnet:
# 2 ** 3
# Was kommt dabei heraus?
# Benutzt den Operator: **

# Wichtige Hinweise:
# - Benutzt Variablen
# - Nutzt print() für die Ausgabe
# - Führt euer Programm nach jedem Schritt aus
# - Achtet auf die richtige Schreibweise der Operatoren

