# Aufgabe: Das Pokémon-Verwaltungssystem

# In der Pokémon-Welt herrscht heute Chaos.
# Schreibt ein Python-Programm, das dem Pokémon-Center hilft.

# 1. Speichert folgende Pokémon in Variablen:
# - 6 Pikachu
# - 4 Glumanda
# - 3 Schiggy

# 2. Berechnet:
# Wie viele Pokémon insgesamt im Pokémon-Center sind.
# Benutzt den Operator: +

# 3. Freigelassene Pokémon
# 5 Pokémon reisen wieder mit ihren Trainern weiter.
# Berechnet:
# Wie viele Pokémon noch im Center bleiben.
# Benutzt den Operator: -

# 4. Pokériegel berechnen
# Jedes Pokémon bekommt 3 Pokériegel.
# Berechnet:
# Wie viele Pokériegel insgesamt benötigt werden.
# Benutzt den Operator: *

# 5. Pokériegel gerecht verteilen
# Berechnet:
# Wie viele Pokériegel jedes Pokémon bekommt.
# Benutzt den Operator: /

# 6. Rest berechnen
# Die Pokériegel werden in 5er-Boxen verpackt.
# Berechnet:
# Wie viele Pokériegel übrig bleiben.
# Benutzt den Operator: %

# 7. Pokémon-Energie
# Berechnet:
# 2 ** 3
# Was kommt dabei heraus?
# Benutzt den Operator: **

