# AUFGABE 1
# Erstelle eine Funktion:
#
# begruessung()
#
# Ausgabe:
# "Willkommen in Mittelerde!"


# AUFGABE 2
# Erstelle eine Funktion:
#
# begruesse_gefaehrte(name)
#
# Beispiel:
# "Hallo Frodo, die Reise zum Schicksalsberg beginnt!"


# AUFGABE 3
# Erstelle eine Funktion:
#
# berechne_kampfkraft(angriff, mut_bonus)
#
# Die Funktion soll angriff + mut_bonus berechnen
# und das Ergebnis zurückgeben.


# AUFGABE 4
# Falls die Kampfkraft größer als 20 ist,
# soll ausgegeben werden:
#
# "Mächtiger Schlag gegen Mordor!"


# AUFGABE 5
# Erstelle eine Funktion:
#
# pruefe_item(inventar, item)
#
# Sie soll prüfen, ob ein Item im Dictionary vorhanden ist.
#
# Beispiel-Items:
# "Lembasbrot"
# "Elbenseil"
# "Schwert"


# AUFGABE 6
# Erstelle eine Funktion:
#
# level_up(level)
#
# Sie soll das Level um 1 erhöhen
# und das neue Level zurückgeben.


# AUFGABE 7
# Erstelle eine Funktion:
#
# kampf_gegen_orc(name, kampfkraft)
#
# Falls kampfkraft >= 50:
# "Der Orc wurde besiegt!"
#
# Sonst:
# "Der Orc greift weiter an!"


