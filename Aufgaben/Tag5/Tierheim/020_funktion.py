# =====================================================
# DAS TIERHEIM-FUNKTIONS-SYSTEM
#
# Ziel:
# Trainiere Funktionen mit einem kleinen Tierheim-Spiel.
# =====================================================


# AUFGABE 1
# Erstelle eine Funktion:
#
# begruessung()
#
# Ausgabe:
# "Willkommen im Tierheim-System!"


# AUFGABE 2
# Erstelle eine Funktion:
#
# begruesse_pfleger(name)
#
# Beispiel:
# "Hallo Mia, die Tiere warten auf dich."


# AUFGABE 3
# Erstelle eine Funktion:
#
# berechne_pflegepunkte(futter, liebe_bonus)
#
# Die Funktion soll futter + liebe_bonus berechnen
# und zurückgeben.


# AUFGABE 4
# Falls die Pflegepunkte größer als 20 sind:
#
# "Die Tiere sind sehr glücklich!"


# AUFGABE 5
# Erstelle eine Funktion:
#
# pruefe_item(lager, item)
#
# Sie soll prüfen, ob ein Item im Dictionary vorhanden ist.
#
# Beispiel-Items:
# "Katzenfutter"
# "Decken"
# "Spielzeug"


# AUFGABE 6
# Erstelle eine Funktion:
#
# erfahrung_up(erfahrung)
#
# Sie soll die Erfahrung um 1 erhöhen
# und zurückgeben.


# AUFGABE 7
# Erstelle eine Funktion:
#
# tier_versorgen(name, pflegepunkte)
#
# Falls pflegepunkte >= 50:
# "Alle Tiere wurden erfolgreich versorgt!"
#
# Sonst:
# "Einige Tiere brauchen noch Hilfe."


