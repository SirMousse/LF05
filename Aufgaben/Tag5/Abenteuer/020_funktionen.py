# =====================================================
# DAS ABENTEUER DER FUNKTIONEN
#
# Ziel:
# Lerne Funktionen mithilfe eines kleinen
# Abenteuer-Systems kennen.
#
# Arbeite möglichst nur mit Funktionen.
# =====================================================


# =====================================================
# AUFGABE 1
# DIE BEGRÜSSUNG
#
# Erstelle eine Funktion:
#
# begruessung()
#
# Die Funktion soll ausgeben:
#
# "Willkommen in den vergessenen Ruinen!"
#
# Rufe die Funktion danach auf.
# =====================================================



# =====================================================
# AUFGABE 2
# DER HELD
#
# Erstelle eine Funktion:
#
# begruesse_held(name)
#
# Die Funktion soll einen Namen entgegennehmen
# und z.B. ausgeben:
#
# "Hallo Arkon, dein Abenteuer beginnt!"
#
# Teste die Funktion mit mehreren Namen.
# =====================================================



# =====================================================
# AUFGABE 3
# DAS KAMPFSYSTEM
#
# Erstelle eine Funktion:
#
# berechne_schaden(angriff, bonus)
#
# Die Funktion soll:
#
# angriff + bonus
#
# berechnen und das Ergebnis zurückgeben.
#
# Beispiel:
#
# berechne_schaden(12, 5)
#
# Ergebnis:
# 17
# =====================================================



# =====================================================
# AUFGABE 4
# KRITISCHER TREFFER
#
# Erweitere die Funktion:
#
# Falls der Schaden größer als 20 ist,
# soll zusätzlich ausgegeben werden:
#
# "Kritischer Treffer!"
# =====================================================



# =====================================================
# AUFGABE 5
# DAS INVENTAR
#
# Erstelle eine Funktion:
#
# pruefe_item(inventar, item)
#
# Die Funktion soll prüfen,
# ob ein bestimmtes Item
# im Dictionary vorhanden ist.
#
# Beispiel:
#
# inventar = {
#     "Schwert": 1,
#     "Heiltrank": 3
# }
#
# Falls das Item existiert:
#
# "Item gefunden!"
#
# Sonst:
#
# "Item nicht gefunden!"
# =====================================================



# =====================================================
# AUFGABE 6
# LEVEL-UP
#
# Erstelle eine Funktion:
#
# level_up(level)
#
# Die Funktion soll:
#
# - das Level um 1 erhöhen
# - das neue Level zurückgeben
#
# Beispiel:
#
# level_up(5)
#
# Ergebnis:
# 6
# =====================================================



# =====================================================
# AUFGABE 7
# DER BOSSKAMPF
#
# Erstelle eine Funktion:
#
# bosskampf(name, schaden)
#
# Die Funktion soll:
#
# - den Namen des Spielers anzeigen
# - den verursachten Schaden anzeigen
#
# Falls der Schaden >= 50:
#
# "Der Drachenwächter wurde besiegt!"
#
# Sonst:
#
# "Der Drachenwächter kämpft weiter!"
# =====================================================



# =====================================================
# BONUS-CHALLENGE
#
# Verbinde mehrere Funktionen miteinander.
#
# Beispiel:
#
# - Spieler begrüßen
# - Schaden berechnen
# - Level erhöhen
# - Inventar prüfen
# - Bosskampf starten
#
# Baue daraus ein kleines Text-Abenteuer.
# =====================================================