# Aufgabe: Das Zelda-Verwaltungssystem

# In Hyrule herrscht heute Chaos.
# Schreibt ein Python-Programm, das Link hilft.

# 1. Speichert folgende Dinge in Variablen:
# - 6 Rubine
# - 4 Herzen
# - 3 Pfeile

# 2. Berechnet:
# Wie viele Gegenstände Link insgesamt hat.
# Benutzt den Operator: +

# 3. Verlorene Gegenstände
# Link verliert 5 Gegenstände im Kampf gegen Monster.
# Berechnet:
# Wie viele Gegenstände noch übrig bleiben.
# Benutzt den Operator: -

# 4. Bomben berechnen
# Für jeden Gegenstand findet Link 3 Bomben.
# Berechnet:
# Wie viele Bomben Link insgesamt findet.
# Benutzt den Operator: *

# 5. Bomben gerecht verteilen
# Berechnet:
# Wie viele Bomben jeder Gegenstand bekommen würde.
# Benutzt den Operator: /

# 6. Rest berechnen
# Die Bomben werden in 5er-Beutel gepackt.
# Berechnet:
# Wie viele Bomben übrig bleiben.
# Benutzt den Operator: %

# 7. Magische Kraft
# Berechnet:
# 2 ** 3
# Was kommt dabei heraus?
# Benutzt den Operator: **

# Wichtige Hinweise:
# - Benutzt Variablen
# - Nutzt print() für die Ausgabe
# - Führt euer Programm nach jedem Schritt aus
# - Achtet auf die richtige Schreibweise der Operatoren

