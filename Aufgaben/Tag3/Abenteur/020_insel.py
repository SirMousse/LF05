# Projekt-Aufgabe: Die verbotene Insel
#
# Erstelle ein Abenteuer-Spiel.
#
# Nutze:
# - if
# - elif
# - else
# - nested ifs
#
# Anforderungen:
#
# 1. Frage den Namen des Abenteurers ab.
# 2. Frage das Level ab.
# 3. Frage nach Goldmünzen.
#
# Wenn Level unter 10:
# Ausgabe -> "Du bist noch nicht bereit für die Insel."
#
# Wenn Level zwischen 10 und 29:
# Ausgabe -> "Du darfst die Küste betreten."
#
# Wenn Level ab 30:
# Ausgabe -> "Du darfst zum Tempel der Insel reisen."
#
# 4. Nur wenn Level ab 30 ist:
#    Frage, ob der Spieler eine Karte besitzt: ja/nein
#
#    Wenn ja:
#    Ausgabe -> "Du findest den versteckten Tempel."
#
#    Wenn nein:
#    Ausgabe -> "Du verirrst dich im Dschungel."
#
# 5. Frage nach einer Waffe:
#    - Schwert
#    - Bogen
#    - Magie
#
# 6. Je nach Waffe soll ein anderer Kampf-Ausgang entstehen.
#
# Bonus:
# Wenn Goldmünzen ab 100 sind:
#     Frage, ob der Spieler einen Heiltrank kaufen möchte.
#
# Ziel: Mehrere Wege und mindestens 4 verschiedene Enden.