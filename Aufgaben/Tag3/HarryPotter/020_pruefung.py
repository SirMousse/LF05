# Projekt-Aufgabe: Die Prüfung in Hogwarts
#
# Erstelle ein kleines Text-Adventure.
#
# Nutze:
# - if
# - elif
# - else
# - nested ifs
#
# Anforderungen:
#
# 1. Frage den Namen des Schülers ab.
# 2. Frage das Haus ab: Gryffindor, Slytherin, Ravenclaw oder Hufflepuff.
# 3. Frage die Punktzahl in Zauberkunde ab.
#
# Wenn die Punktzahl unter 50 ist:
# Ausgabe -> "Du musst zur Nachprüfung."
#
# Wenn die Punktzahl zwischen 50 und 79 ist:
# Ausgabe -> "Du hast bestanden."
#
# Wenn die Punktzahl ab 80 ist:
# Ausgabe -> "Du darfst zur geheimen Prüfung antreten."
#
# 4. Nur wenn die geheime Prüfung erlaubt ist:
#    Frage nach dem Zauberstab-Level.
#
#    Wenn Zauberstab-Level ab 70:
#    Ausgabe -> "Dein Zauberstab leuchtet hell."
#
#    Sonst:
#    Ausgabe -> "Dein Zauberstab ist noch nicht stark genug."
#
# 5. Frage nach einem Zauberspruch:
#    - Expelliarmus
#    - Lumos
#    - Expecto Patronum
#
# 6. Je nach Zauberspruch soll etwas anderes passieren.
#
# Bonus:
# Baue ein nested if ein:
#
# Wenn Haus == "Gryffindor":
#     Wenn Punktzahl >= 80:
#         Ausgabe -> "Mutiger Gryffindor besteht die Prüfung!"
#
# Ziel: Erstelle mehrere verschiedene Enden.