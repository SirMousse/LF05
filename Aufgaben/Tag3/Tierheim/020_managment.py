# Projekt-Aufgabe: Das Tierheim-Management
#
# Erstelle ein kleines Tierheim-Programm.
#
# Nutze:
# - if
# - elif
# - else
# - nested ifs
#
# Anforderungen:
#
# 1. Frage den Namen des Mitarbeiters ab.
#
# 2. Frage nach der Anzahl freier Plätze im Tierheim.
#
# 3. Frage nach der Anzahl kranker Tiere.
#
# Wenn freie Plätze unter 5:
# Ausgabe -> "Das Tierheim ist fast voll."
#
# Wenn freie Plätze zwischen 5 und 15:
# Ausgabe -> "Es sind noch einige Plätze frei."
#
# Wenn freie Plätze über 15:
# Ausgabe -> "Viele Plätze verfügbar."
#
# 4. Nur wenn freie Plätze über 0 sind:
#    Frage, welches Tier aufgenommen werden soll:
#
#    - Hund
#    - Katze
#    - Kaninchen
#
# 5. Je nach Tier soll eine andere Ausgabe erscheinen.
#
# Beispiel:
#
# Hund:
# "Der Hund bekommt einen großen Auslauf."
#
# Katze:
# "Die Katze kommt ins Katzenzimmer."
#
# Kaninchen:
# "Das Kaninchen bekommt frisches Heu."
#
#
# 6. Frage nun nach dem Gesundheitszustand:
#
#    - gesund
#    - verletzt
#    - kritisch
#
# 7. Nutze nested ifs:
#
# Beispiel:
#
# Wenn Tier == Hund:
#
#     Wenn Gesundheitszustand == verletzt:
#         Ausgabe -> "Der Hund wird sofort behandelt."
#
#
# 8. Wenn kranke Tiere über 10 sind:
#    Ausgabe -> "Die Tierarztstation ist überlastet."
#
# 9. Frage, ob genug Futter vorhanden ist: ja/nein
#
# Wenn nein:
# Ausgabe -> "Es muss dringend neues Futter bestellt werden."
#
# Wenn ja:
# Ausgabe -> "Alle Tiere können versorgt werden."
#
#
# Bonus-Aufgaben:
#
# BONUS 1:
# Frage nach der Anzahl der freiwilligen Helfer.
#
# Wenn Helfer über 5:
# Ausgabe -> "Das Tierheim hat genug Unterstützung."
#
# Sonst:
# Ausgabe -> "Es werden weitere Helfer benötigt."
#
#
# BONUS 2:
# Frage nach dem Alter eines Tieres.
#
# Wenn Alter unter 2:
# Ausgabe -> "Das Tier ist noch jung."
#
# Wenn Alter zwischen 2 und 8:
# Ausgabe -> "Das Tier ist erwachsen."
#
# Wenn Alter über 8:
# Ausgabe -> "Das Tier ist bereits älter."
#
#
# Ziel:
# Erstelle ein realistisches Tierheim-Management
# mit vielen Entscheidungen und mehreren möglichen Enden.