# Projekt-Aufgabe: Die Reise nach Mordor
#
# Erstelle ein Fantasy-Abenteuer.
#
# Nutze:
# - if
# - elif
# - else
# - nested ifs
#
# Anforderungen:
#
# 1. Frage den Namen des Helden ab.
# 2. Frage die Stärke ab.
# 3. Frage die Anzahl der Gefährten ab.
#
# Wenn Stärke unter 30:
# Ausgabe -> "Du bist zu schwach für diese Reise."
#
# Wenn Stärke zwischen 30 und 69:
# Ausgabe -> "Du erreichst die Berge."
#
# Wenn Stärke ab 70:
# Ausgabe -> "Du erreichst Mordor."
#
# 4. Nur wenn Stärke ab 70 ist:
#    Frage, ob der Ring getragen wird: ja/nein
#
#    Wenn ja:
#    Ausgabe -> "Der Ring wird schwerer."
#
#    Wenn nein:
#    Ausgabe -> "Du widerstehst der Macht des Rings."
#
# 5. Frage nach dem Begleiter:
#    - Gandalf
#    - Aragorn
#    - Legolas
#
# 6. Je nach Begleiter soll ein anderes Ereignis passieren.
#
# Bonus:
# Wenn Gefährten ab 5 sind:
#     Ausgabe -> "Die Gemeinschaft ist stark."
#
# Ziel:
# Erstelle mindestens 4 verschiedene Enden.