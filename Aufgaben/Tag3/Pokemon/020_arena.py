# Projekt-Aufgabe: Die Arena-Challenge
#
# Erstelle ein Pokémon-ähnliches Textspiel.
#
# Nutze:
# - if
# - elif
# - else
# - nested ifs
#
# Anforderungen:
#
# 1. Frage den Namen des Trainers ab.
# 2. Frage die Anzahl der Orden ab.
# 3. Frage das Level des stärksten Pokémon ab.
#
# Wenn Orden unter 4:
# Ausgabe -> "Du darfst noch nicht zur Liga."
#
# Wenn Orden zwischen 4 und 7:
# Ausgabe -> "Du darfst stärkere Arenen herausfordern."
#
# Wenn Orden ab 8:
# Ausgabe -> "Pokémon Liga freigeschaltet."
#
# 4. Nur wenn Orden ab 8 sind:
#    Frage, ob der Trainer Heilitems besitzt: ja/nein
#
#    Wenn ja:
#    Ausgabe -> "Du bist bereit für die Liga."
#
#    Wenn nein:
#    Ausgabe -> "Du solltest dich besser vorbereiten."
#
# 5. Frage nach dem Pokémon-Typ:
#    - Feuer
#    - Wasser
#    - Pflanze
#
# 6. Je nach Typ soll ein anderer Kampf stattfinden.
#
# Bonus:
# Wenn Pokémon-Level ab 70 ist:
#     Ausgabe -> "Dein Pokémon ist sehr stark."
#
# Ziel:
# Erstelle mehrere Kampf-Ausgänge.